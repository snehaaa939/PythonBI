#Q. Calculate the compound interest for amount Rs. 50,000 at rate 6.5% p.a compounded annually for 4 years. CI = P(1 + r/n)^(nt) - P
principal = 50000
rate = 6.5
time = 4
compound_interest = principal * (1 + rate/100) ** time - principal
print("The compound interest for 4 years is:", compound_interest)